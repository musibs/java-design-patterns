package com.codefountain.design.patterns.structural.adapter.sample;

public class Volt {

    private int voltValue;

    public Volt(int voltValue) {
        this.voltValue = voltValue;
    }

    public int getVoltValue() {
        return voltValue;
    }

    public void setVoltValue(int voltValue) {
        this.voltValue = voltValue;
    }
}
